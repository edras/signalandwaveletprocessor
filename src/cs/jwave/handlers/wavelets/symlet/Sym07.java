package cs.jwave.handlers.wavelets.symlet;

import cs.jwave.handlers.wavelets.Wavelet;

/**
 * Ingrid Daubechies' orthonormal Symlet wavelet of fourteen coefficients and the
 * scales; normed, due to ||*||2 - euclidean norm.
 * 
 * @date 10.02.2010 16:32:38
 * @author Christian Scheiblich
 */
public class Sym07 extends Wavelet {

  /**
   * Constructor setting up the orthonormal Symlet07 wavelet coeffs and the
   * scales; normed, due to ||*||2 - euclidean norm.
   * 
   * @date 10.02.2010 16:32:38
   * @author Christian Scheiblich
   */
  public Sym07( ) {

    _waveLength = 14; // minimal array size for transform

    double[ ] scales = {
        0.010268176708511255,
        0.0040102448715336634,
        -0.10780823770381774,
        -0.14004724044296152,
        0.28862963175151463,
        0.76776431700316405,
        0.5361019170917628,
        0.017441255086855827,
        -0.049552834937127255,
        0.067892693501372697,
        0.03051551316596357,
        -0.01263630340325193,
        -0.0010473848886829163,
        0.0026818145682578781
    };
    
    _scales = new double[ _waveLength ]; // can be done in static way also; faster?
    
    for( int i = 0; i < _waveLength; i++ )
      _scales[ i ] = scales[ i ];
    
    _coeffs = new double[ _waveLength ]; 

    for( int i = 0; i < _waveLength; i++ )
      if( ( i % 2 ) == 0 ) {
        _coeffs[ i ] = _scales[ ( _waveLength - 1 ) - i ];
      } else {
        _coeffs[ i ] = -_scales[ ( _waveLength - 1 ) - i ];
      } // if

  } // Sym07

} // class
